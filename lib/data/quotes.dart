/// Returns today's quote based on the day of the year.
Quote getDailyQuote() {
  final now = DateTime.now();
  final dayOfYear = now.difference(DateTime(now.year, 1, 1)).inDays;
  return motivationQuotes[dayOfYear % motivationQuotes.length];
}
